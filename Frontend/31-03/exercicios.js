//length - Retorna o comprimento da string em caracteres.
let str1 = "Kaspper Academy";
console.log(str1.length); //retorno = 15

//charAt() - Retorna o caractere na posição especificada.
let str2 = "Kaspper Academy";
console.log(str2.charAt(6)); // Retorno = r

//toUpperCase() - Converte a string para maiúsculas.
let str3 = "Kaspper Academy";
console.log(str3.toUpperCase()); // Retorno = KASPPER ACADEMY

// indexOf() - Retorna o índice da primeira ocorrência do valor especificado.
let str4 = "Kaspper Academy";
console.log(str4.indexOf("Academy")); // Retorno = 8

//slice() - Retorna uma parte da string, começando no índice especificado e terminando em outro índice especificado (ou até o final da string).
let str5 = "Kaspper Academy";
console.log(str5.slice(2, 6)); // Retono = sppe



